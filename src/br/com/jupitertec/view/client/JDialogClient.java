    private void formWindowActivated(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowActivated

        String path = "Fotos\\1.png";

        if (!System.getProperty("os.name").contains("Windows")) {

            path = path.replace("\\", "//");

        }

        try {
            File picture = new File(path);
            
            if(picture.exists())
                jLabelFoto.setIcon(new ImageIcon(ImageIO.read(picture).getScaledInstance(150, 200, Image.SCALE_AREA_AVERAGING + Image.SCALE_SMOOTH)));
        } catch (IOException ex) {
            Logger.getLogger(JDialogClient.class.getName()).log(Level.SEVERE, null, ex);
        }
        jTextFieldName.grabFocus();
    }//GEN-LAST:event_formWindowActivated

