new SwingWorker() {
            @Override
            protected Void doInBackground() throws Exception {
                // Simulate doing something useful. 
                for (int i = 0; i <= 101; i++) {
                    jProgressBarCEP.setVisible(true);
                    jProgressBarCEP.setValue(i);
                    sleep(23);

                }
                jProgressBarCEP.setVisible(false);
                return null;
            }
        }.execute();

        new SwingWorker() {
            @Override
            protected Void doInBackground() throws Exception {

                try {
                    String[] resultado = findCEP.getAllDataCep(jFormattedTextFieldCep.getText());

                    jTextFieldAddress.setText(resultado[0]);
                    jTextFieldDistrict.setText(resultado[1]);
                    jComboBoxCountry.setModel(new javax.swing.DefaultComboBoxModel(new String[]{resultado[2]}));
                    jComboBoxState.setModel(new javax.swing.DefaultComboBoxModel(new String[]{resultado[3]}));

                } catch (IOException ex) {
                    JOptionPane.showMessageDialog(null, "Ops! Não encontrei o CEP: " + jFormattedTextFieldCep.getText());
                }

                if (jTextFieldAddress.getText().equals(jFormattedTextFieldCep.getText())) {

                    jTextFieldAddress.setText(null);
                    jTextFieldDistrict.setText(null);
                    jComboBoxCountry.setModel(new javax.swing.DefaultComboBoxModel(new String[]{""}));
                    jComboBoxState.setModel(new javax.swing.DefaultComboBoxModel(new String[]{""}));

                    JOptionPane.showMessageDialog(null, "Ops! Não encontrei o CEP: " + jFormattedTextFieldCep.getText());

                }

                return null;
            }
        }.execute();
        
        
        
